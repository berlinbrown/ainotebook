package org.berlin.pino.dev.analy.model;

public class BasicFileInfo {

    private int numLines = 0;
    private int numLinesSrc = 0;
    
    /**
     * @return the numLines
     */
    public int getNumLines() {
        return numLines;
    }

    /**
     * @param numLines the numLines to set
     */
    public void setNumLines(int numLines) {
        this.numLines = numLines;
    }

    /**
     * @return the numLinesSrc
     */
    public int getNumLinesSrc() {
        return numLinesSrc;
    }

    /**
     * @param numLinesSrc the numLinesSrc to set
     */
    public void setNumLinesSrc(int numLinesSrc) {
        this.numLinesSrc = numLinesSrc;
    }
    
} // End of the class //
